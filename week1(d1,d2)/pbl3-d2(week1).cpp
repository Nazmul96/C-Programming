/*
        Time Duration: 5 minutes
        3. Write a C program to check whether a number is negative, positive, or zero.
*/
#include <stdio.h>

int main()
{
    int number;
    scanf("%d",&number);
    if(number>0){
        printf("positive number.\n");
    }
    else if(number<0){
        printf("negative number.\n");
    }
    if(number==0){
        printf("%d\n",number);
    }

    return 0;
}

